class Centurion_Stack  {  
    
    /**
     * @var array Used in serialization
     */
    private $_data = array();
 

    public function push($data){
        array_push($this->_data,$data);
    }
    
    public function pop(){
        return array_pop($this->_data);
    }
    
    public function isEmpty(){
        return empty($this->_data);
    }
    
}
